CREATE SEQUENCE account_number_seq
    START WITH 0000000001
    INCREMENT BY 1;

CREATE TABLE IF NOT EXISTS accounts(
    id  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    acocunt_holder_name VARCHAR(255) NOT NULL,
    account_number BIGINT NOT NULL UNIQUE,
    email VARCHAR(100) NOT NULL UNIQUE,
    phone_number VARCHAR(100) NOT NULL,
    account_type VARCHAR(20) NOT NULL CHECK(account_type IN('SAVINGS','CURRENT','SALARY','FIXED_DEPOSIT')),
    account_satus VARCHAR(20) NOT NULL CHECK(account_status IN('ACTIVE','BLOCKED','CLOSED')),
    balance NUMERIC(15,2) NOT NULL DEFAULT 0.00 CHECK(balance >= 0),
    daily_transaction_limit NUMERIC(15,2) NOT NULL CHECK (daily_transaction_limit >= 0),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP

)